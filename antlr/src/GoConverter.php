	 * {@inheritdoc}
	 *
	 * The default implementation returns the result of calling
	 * {@see self::visitChildren()} on `context`.
	 */
	public function visitSourceFile(Context\SourceFileContext $context)
	{
	    $file = new File();
        $file->functions = array_reduce($context->functionDecl(), function ($result, FunctionDeclContext $func) {
            /** @var Function_ $fn */
            $fn = $func->accept($this);
            $result[$fn->name] = $fn;
            return $result;
        }, []);
        $file->packages = $context->packageClause()->accept($this);
	    return $file;
	}

